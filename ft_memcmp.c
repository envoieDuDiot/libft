#include "libft.h"

int		ft_memcmp(const void *str1, const void *str2, size_t len)
{
	const unsigned char *ptr1;
	const unsigned char *ptr2;

	ptr1 = str1;
	ptr2 = str2;
	while (len--)
	{
		if (*ptr1 != *ptr2)
			return (*ptr1 - *ptr2);
		else
		{
			ptr1++;
			ptr2++;
		}
	}
	return (0);
}

#include "libft.h"

void	ft_putnbr_fd(int i, int fd)
{
	long	nb;

	nb = i;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb *= (-1);
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
		ft_putchar_fd(nb % 10 + '0', fd);
}
